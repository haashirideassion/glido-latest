export function findBooking(id: string): Booking | undefined {
  return mockBookings.find((b) => b.id === id || b.referenceNumber === id)
}

export function getTodayBookings(): Booking[] {
  return mockBookings.filter(b => b.slotDate === '2026-05-12')
}

export function getBookingsByDate(date: string): Booking[] {
  return mockBookings.filter((b) => b.slotDate === date)
}

export function getDashboardStats() {
  const today = getTodayBookings()
  return {
    totalScheduled: today.filter(b => b.status === 'scheduled').length,
    checkedIn:      today.filter(b => b.status === 'checked_in').length,
    completed:      today.filter(b => b.status === 'completed').length,
    held:           today.filter(b => b.icsStatus === 'held').length,
  }
}
